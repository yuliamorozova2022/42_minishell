#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	str[10];

	i = 0;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n == 0)
		write(fd, "0", 1);
	else
	{
		if (n < 0)
		{
			write (fd, "-", 1);
			n *= -1;
		}
		while (n > 0)
		{
			str[i] = (n % 10) + 48;
			n = n / 10;
			i++;
		}
		i--;
		while (i >= 0)
			write(fd, &str[i--], 1);
	}
}
